public class Buchstaben {
    public static void main(String[] args) {
        //Variablen deklarieren
        String text;
        String tabsprung;
        
        //Variablen initialisieren
        text = "Hallo, Welt in einer Variablen";
        tabsprung = "Text\tmit\tTab\tgetrennt";

        //Variablen ausgeben
        System.out.println(text);
        System.out.println(tabsprung);
    }
}
